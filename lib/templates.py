#   wait = (resetsAt - now) + jitter, where jitter is a uniform random
#   0..usage_limit_wait_jitter_seconds ADDED ON TOP (never subtracted). If
#   wait > usage_limit_max_wait_seconds: do NOT sleep - halt, writing
#   `usage-limit-reset-abandoned` into loop-final-$TASK_ID.md and surfacing
