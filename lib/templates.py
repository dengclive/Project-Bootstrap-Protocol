#  Phase {phase}] The operator-completed loop writes the audit record to
#     .claude/sessions/loop-final-$TASK_ID.md
#  at exit. It is an operator-facing audit record and is COMMITTED: the
#  gitignore fragment ignores only the .claude/sessions/ dotfile sentinels
#  (.loop-active-*, .session-* and friends), never loop-final-*.
#  Required lines:
#     Status:      <completed | halted:<cause> | max-iterations>
#     Iterations:  <n of max>
#     Trajectory:  links to the retained
#                  .claude/logs/trajectory-$TASK_ID-*.jsonl files (one per
#                  iteration; the gitignored raw per-iteration record this
