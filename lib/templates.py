# NOT reach .claude/logs/, and nothing in the emitted tree prunes trajectory
# files. Retained stream JSON carries full tool I/O and grows without bound
# across an unattended campaign, so the operator-completed loop MUST prune it
# on a stated retention window (7 days matches the state policy) - and any
# telemetry/privacy review MUST be told the real retention, not an inherited
# one.
#
# [Usage-limit vs transient-failure handling - Bootstrap-Protocol-v2-2-0.md
#  Phase {phase} "Infrastructure-error handling", AR2-corrected. BINDING on the
#  operator-completed iteration loop; the bare skeleton dispatches nothing.]
#
# The loop dispatches `claude -p` with --output-format stream-json --verbose
# (see the [IC-6] header) and TAILS the newline-delimited (NDJSON) event
# stream. Claude Code emits a usage-limit signal as a stream line whose
# TOP-LEVEL `type` field is "rate_limit_event". MATCH IT BY THAT top-level
# `type` field - NEVER by substring-searching the stream, since assistant
# text can contain the literal string "rate_limit_event".
#
# Wire keys are camelCase inside the nested `rate_limit_info` object (these
# are the raw stream keys, NOT the Python SDK's snake_case dataclass names
# resets_at / rate_limit_type):
#     status         "allowed" | "allowed_warning" | "rejected"
#     resetsAt       Unix seconds when the bucket refills; MAY BE ABSENT
#     rateLimitType  five_hour | seven_day | seven_day_opus | seven_day_sonnet | overage
# Record the most recent rate_limit_event seen before the exit.
#
# On a NON-EXPECTED non-zero `claude -p` exit (not completion/tier-3/halt),
# classify from that last event:
#   * status "rejected" AND a FUTURE resetsAt          -> usage-limit path.
#   * status "rejected" with resetsAt ABSENT or PAST   -> transient path.
#   * no "rejected" rate_limit_event at all (network error, 5xx, a 529
#     overload)                                        -> transient path.
#
