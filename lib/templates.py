def _auto_sh(cfg):
    # F-2 fix: auto.sh is a terminal-invoked runner, NOT a stdin-fed hook.
    # It must not inherit `INPUT="$(cat)"` (which blocks waiting for EOF) or
    # the jget machinery. The trap records a Bootstrap-Protocol-v2-0-0.md exit_reason rather
    # than blanket `exit 0`, and only the EXIT arm cleans the sentinel.
    return '''#!/usr/bin/env bash
# Generated by bootstrap-installer. Autonomous queue runner (Phase 9.7).
# SKELETON: the dispatch loop is intentionally unimplemented - see README and
# Bootstrap-Protocol-v2-0-0.md Phase 9.7. Do not use unattended until the loop and the
# exit_reason accounting below are completed and smoke-tested.
set -euo pipefail
# Self-locate: this script lives at <project>/.claude/auto.sh, so the project
# root is two levels up. Honour CLAUDE_PROJECT_DIR if set, else derive it -
# never assume the invocation cwd is the project root (F-2 follow-up).
SCRIPT_DIR="$(cd "$(dirname "${BASH_SOURCE[0]:-$0}")" && pwd)"
PROJ="${CLAUDE_PROJECT_DIR:-$(cd "$SCRIPT_DIR/.." && pwd)}"
LOG="$PROJ/.claude/logs/hooks.log"
mkdir -p "$(dirname "$LOG")"
log(){ printf '%s %s\\n' "$(date -u +%FT%TZ)" "$*" >>"$LOG"; }

RUN="$PROJ/.claude/queue/.run-active"
mkdir -p "$(dirname "$RUN")"            # queue dir may not exist yet

HALT="$PROJ/.claude/queue/.halt"       # legacy queue-scoped halt (remains)
# IC-2 root sentinels (protocol 2.0.0; dual-honor is PERMANENT):
#   $PROJ/.halt       graceful stop at the next boundary, honoured IN
#                     ADDITION to the queue-scoped .halt above (back-compat).
#   $PROJ/.halt-hard  immediate wrapper exit. The wrapper does NOT signal any
#                     in-flight `claude -p`; killing processes is the
#                     caller's job. The operator-completed dispatch loop MUST
#                     re-check both sentinels at every iteration boundary.
ROOT_HALT="$PROJ/.halt"
ROOT_HALT_HARD="$PROJ/.halt-hard"
# Checked before the cleanup trap is installed: a halt refusal must never
# touch another run's .run-active sentinel.
if [ -e "$ROOT_HALT_HARD" ]; then
  echo "Hard-halt sentinel present ($ROOT_HALT_HARD). Exiting immediately." >&2
  log "auto.sh exit reason=manual-halt-sentinel (root hard-halt)"
  exit 1
fi
if [ -e "$ROOT_HALT" ] || [ -e "$HALT" ]; then
  echo "Halt sentinel present (root .halt or queue .halt). Refusing to start." >&2
  log "auto.sh exit reason=manual-halt-sentinel (graceful halt)"
  exit 1
fi

EXIT_REASON="infrastructure-failure"   # pessimistic default; set on clean paths

CLAIMED=0
cleanup(){
  rc=$?
  # Only remove the sentinel if WE claimed it (never yank another runner's).
  # Phase 9.7: a refusing loser must leave the winner's sentinel intact -
  # a deleted winner sentinel would let a third invocation start a
  # concurrent runner past the combined-concurrency cap.
  [ "$CLAIMED" = 1 ] && rm -f "$RUN"
  log "auto.sh exit reason=$EXIT_REASON rc=$rc"
  # NOTE: a complete implementation updates the dangling queue_runs_history
  # entry here with end_timestamp and exit_reason before exiting.
  exit "$rc"
}
on_signal(){ EXIT_REASON="signal-interrupt"; exit 130; }
trap cleanup EXIT
trap on_signal INT TERM

# Sentinel-parse helpers. The `|| true` inside the pipelines keeps a sed
# failure (unreadable sentinel, sentinel-is-a-directory, file racing away
# mid-read) from killing the script via errexit+pipefail before the
# fail-safe branches below can classify it - the helpers yield empty.
run_pid(){ { sed -n 's/^pid=\\([0-9]\\{1,\\}\\).*/\\1/p' "$RUN" 2>/dev/null || true; } | head -1; }
run_start(){ { sed -n 's/^.*start=//p' "$RUN" 2>/dev/null || true; } | head -1; }
# Three-state liveness: 0=alive, 1=dead, 2=cannot-determine. `ps -p` is
# probed against our own PID first: on a platform whose ps lacks -p
# (busybox), the self-probe fails and we fall back to kill -0, whose
# SUCCESS proves aliveness but whose failure may be EPERM (live process,
# other user) - that is cannot-determine, never "dead". Fail-safe
# direction: cannot-determine lands on refuse, never on stale-clear.
pid_alive(){
  if ps -p $$ >/dev/null 2>&1; then
    if ps -p "$1" >/dev/null 2>&1; then return 0; else return 1; fi
  fi
  kill -0 "$1" 2>/dev/null && return 0
  return 2
}

# Startup mutual exclusion (Phase 9.7: "Two auto.sh processes started
# within the same second must not both pass this check"): the whole
# check -> operator-confirmed clear -> O_CREAT|O_EXCL claim sequence runs
# under flock, so two interactive invocations can never both clear the
# same stale sentinel - the second refuses instantly instead of racing.
# flock is already a hard requirement of the per-task wrappers; same
# fail-safe posture here.
if ! command -v flock >/dev/null 2>&1; then
  echo "flock unavailable - cannot make the sentinel check-and-claim atomic." >&2
  EXIT_REASON="infrastructure-failure"
  exit 1
fi
exec 8>"$RUN.lock"
if ! flock -n 8; then
  echo "Another auto.sh invocation is mid-startup ($RUN.lock). Refusing to start." >&2
  EXIT_REASON="manual-halt-sentinel"
  exit 1
fi

# Phase 9.7 startup check: sentinel-presence alone is not a sufficient
# check - PID-liveness is the actual signal.
if [ -e "$RUN" ]; then
  OLD_PID="$(run_pid)"
  OLD_START="$(run_start)"
  if [ -z "$OLD_PID" ]; then
    echo "Found $RUN but could not read a PID from it. Refusing to start;" >&2
    echo "inspect it and remove it manually if the prior run is gone." >&2
    EXIT_REASON="manual-halt-sentinel"
    exit 1
  fi
  pid_alive "$OLD_PID" && LIVE=0 || LIVE=$?
  if [ "$LIVE" = 0 ]; then
    echo "A queue run is already active (pid=$OLD_PID, $RUN). Refusing to start." >&2
    EXIT_REASON="manual-halt-sentinel"
    exit 1
  fi
  if [ "$LIVE" = 2 ]; then
    echo "Cannot determine whether pid=$OLD_PID is alive on this platform." >&2
    echo "Refusing to start; verify the prior run and remove $RUN manually." >&2
    EXIT_REASON="manual-halt-sentinel"
    exit 1
  fi
  # Stale sentinel from a crashed prior run: alert with the recorded start
  # timestamp and ask before clearing (Phase 9.7). The prompt is offered
  # only on an interactive terminal - a non-tty stdin auto-answers No
  # BEFORE any read - and the read itself is time-bounded, so even a
  # forced prompt on an open-but-silent pipe cannot hang (F-2 class):
  # timeout and EOF both fall through to No. BOOTSTRAP_TEST_FORCE_PROMPT=1
  # is a TEST-ONLY override that forces the prompt path on a non-tty; it
  # can only enable ASKING (the answer is still read from stdin, default
  # No) - it can never clear the sentinel by itself.
  echo "Stale .run-active: pid=$OLD_PID is not alive (started ${OLD_START:-unknown})." >&2
  if [ -t 0 ] || [ "${BOOTSTRAP_TEST_FORCE_PROMPT:-0}" = 1 ]; then
    printf 'Clear the stale sentinel and continue? [y/N] ' >&2
    IFS= read -r -t "${BOOTSTRAP_PROMPT_TIMEOUT:-60}" REPLY || REPLY=""
  else
    echo "Non-interactive invocation: not clearing the stale sentinel." >&2
    REPLY=""
  fi
  case "$REPLY" in
    y|Y) ;;
    *)
      echo "Leaving the stale sentinel in place; remove $RUN manually to proceed." >&2
      EXIT_REASON="manual-halt-sentinel"
      exit 1
      ;;
  esac
  # Re-verify before clearing: auto.sh contenders are already excluded by
  # the startup lock; this guards against NON-auto.sh writers (e.g. a
  # dispatch layer that owns the sentinel path) replacing the sentinel
  # while the operator sat at the prompt.
  NOW_PID="$(run_pid)"
  if [ "$NOW_PID" != "$OLD_PID" ]; then
    echo "The sentinel changed while waiting for confirmation (pid=${NOW_PID:-gone})." >&2
    echo "Another runner claimed it. Aborting without touching it." >&2
    EXIT_REASON="manual-halt-sentinel"
    exit 1
  fi
  rm -f "$RUN"
  log "auto.sh cleared stale .run-active (pid=$OLD_PID start=${OLD_START:-unknown})"
fi
# Race protection (Phase 9.7): O_CREAT|O_EXCL claim. A failure means a
# concurrent runner won the race - abort with a clear message and exit
# non-zero rather than overwriting.
if ! ( set -C; printf 'pid=%s start=%s\\n' "$$" "$(date -u +%FT%TZ)" >"$RUN" ) 2>/dev/null; then
  echo "Another runner won the .run-active race ($RUN). Aborting." >&2
  EXIT_REASON="manual-halt-sentinel"
  exit 1
fi
CLAIMED=1
exec 8>&-                               # release the startup lock
log "auto.sh started (skeleton)."

echo "Queue runner skeleton installed. Implement the dispatch loop per" \\
     "Bootstrap-Protocol-v2-0-0.md Phase 9.7 before any unattended use." >&2
EXIT_REASON="queue-empty"   # skeleton: nothing dispatched, treated as no-op
exit 0
'''


