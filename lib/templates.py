# `.claude/worktrees/` to .git/info/exclude (local, per-clone). One-liner:
#   grep -qxF '.claude/worktrees/' "$PROJ/.git/info/exclude" 2>/dev/null || echo '.claude/worktrees/' >> "$PROJ/.git/info/exclude"
